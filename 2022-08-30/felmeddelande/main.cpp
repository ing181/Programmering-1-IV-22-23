#include <iostream>

using namespace std;

int main(){

    cout << "Mata in ett tal" << endl;
    // En variabel för heltal skapas
    // variabel deklareras
    int tal;
    // Från tangentborde till "tal"
    cin >> tal;
    // Innehållet i variabeln tal
    // matas ut till skärmen
    cout << "Du matade in " << tal << endl;
    // räkneoperatorer +, -, *, /
    cout << "plus 4 blir det " << (tal+4) << endl;
    // Variabel för heltal: int
    // Variabel för flyttal (decimaltal) float
    return 0;
}
